// Experimente a declaração de funções usando diferentes formas, como declaração de função, expressão de função e arrow function.

// função comum

function goth(){
    console.log('Hello baby bat!');
}

goth();


// função anonima

const goth2 = function (){
    console.log('Hello baby bat');
}

goth2();

// função de seta
const goth3 = () => {
    console.log('Hello babybat!');
}

goth3();